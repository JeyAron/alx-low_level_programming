#include <stdio.h>

/**
 * add - adds integers
 * @a: first int
 * @b: second int
 * Return: sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * subtract - subtracts integers
 * @a: first int
 * @b: second int
 * Return: difference
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - divides integers
 * @a: first int
 * @b: second int
 * Return: division
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod integers
 * @a: first int
 * @b: second int
 * Return: mod
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multipliers integers
 * @a: first int
 * @b: second int
 * Return: product
 */

int mul(int a, int b)
{
	return (a * b);
}
